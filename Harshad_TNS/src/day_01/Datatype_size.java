package day_01;

public class Datatype_size {
	public static void main(String[] args) {
		byte b = 127;
		System.out.println("The range of byte data type is = " + b);
		
		short s = 32767;
		System.out.println("The range of short data type is = " + s);
		
		int i = 2147483647;
		System.out.println("The range of int data type is = " + i);
		
		long l = 9223372036854775807l;
		System.out.println("The range of long data type is = " + l);
		
	}

}
